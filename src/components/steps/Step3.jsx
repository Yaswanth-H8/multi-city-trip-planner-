/* ─── PDF generation ─── */
async function generatePDF({ allStops, legs, dateStart, dateEnd, nights, passengers, payingPax, totalPax, totalCostUSD, taxUSD, insUSD, currency, fm, selAirlineName }){
  const { jsPDF } = await import("jspdf");
  const doc = new jsPDF({ unit:"mm", format:"a4" });
  const W = doc.internal.pageSize.getWidth();
  let y = 18;
  const line = (lbl,val,bold=false) => {
    doc.setFont("helvetica", bold?"bold":"normal");
    doc.setFontSize(11);
    doc.text(lbl, 18, y);
    doc.text(val, W-18, y, {align:"right"});
    y += 7;
  };

  // Header bar
  doc.setFillColor(255,140,0);
  doc.rect(0,0,W,36,"F");
  doc.setTextColor(255,255,255);
  doc.setFont("helvetica","bold"); doc.setFontSize(22);
  doc.text("TripForge", 18, 16);
  doc.setFontSize(10); doc.setFont("helvetica","normal");
  doc.text("Your Complete Itinerary", 18, 24);
  doc.text(`Generated: ${new Date().toLocaleDateString()}`, W-18, 24, {align:"right"});
  y = 46;

  // Trip overview
  doc.setTextColor(15,23,42);
  doc.setFont("helvetica","bold"); doc.setFontSize(14);
  doc.text("Trip Overview", 18, y); y+=9;
  doc.setDrawColor(255,140,0); doc.setLineWidth(0.5); doc.line(18,y,W-18,y); y+=7;

  line("Route:", allStops.map(s=>`${s.code}`).join(" → "));
  line("Dates:", `${fmtDate(dateStart)} – ${fmtDate(dateEnd)}`);
  line("Duration:", `${nights} nights`);
  line("Passengers:", `${passengers.adults} adult${passengers.adults!==1?"s":""}, ${passengers.children} child${passengers.children!==1?"ren":""}, ${passengers.infants} infant${passengers.infants!==1?"s":""}`);
  line("Currency:", `${currency.code} (${currency.sym})`);
  line("Airline:", selAirlineName);
  y += 5;

  // Flight legs
  doc.setFont("helvetica","bold"); doc.setFontSize(14);
  doc.text("Flight Legs", 18, y); y+=9;
  doc.setDrawColor(255,140,0); doc.line(18,y,W-18,y); y+=7;
  legs.forEach(l => {
    line(`✈  ${l.label}`, `${fm(l.usd)} /pax  ·  ${fm(l.usd*payingPax)} total`);
  });
  y += 3;

  // Cost breakdown
  doc.setFont("helvetica","bold"); doc.setFontSize(14);
  doc.text("Cost Breakdown", 18, y); y+=9;
  doc.setDrawColor(255,140,0); doc.line(18,y,W-18,y); y+=7;
  legs.forEach(l => line(l.label, fm(l.usd*payingPax)));
  line("Taxes & Fees", fm(taxUSD*payingPax));
  line("Travel Insurance", fm(insUSD*payingPax));
  y += 2;
  doc.setDrawColor(255,140,0); doc.line(18,y,W-18,y); y+=7;
  line("Grand Total (est.)", fm(totalCostUSD), true);
  y += 5;

  // Visa info
  doc.setFont("helvetica","bold"); doc.setFontSize(14);
  doc.text("Visa Information", 18, y); y+=9;
  doc.setDrawColor(255,140,0); doc.line(18,y,W-18,y); y+=7;
  allStops.forEach(s => {
    line(`${s.name} (${s.code})`, `${s.visa} — ${s.visaBadge}`);
  });

  // Footer
  y = doc.internal.pageSize.getHeight() - 14;
  doc.setFontSize(9); doc.setTextColor(148,163,184);
  doc.text("Generated by TripForge · tripforge.app", W/2, y, {align:"center"});

  doc.save(`TripForge_Itinerary_${new Date().toISOString().slice(0,10)}.pdf`);
}

/* ─── Email builder ─── */
function buildMailto({ allStops, legs, dateStart, dateEnd, nights, totalPax, payingPax, totalCostUSD, fm, selAirlineName }){
  const route = allStops.map(s=>s.code).join(" → ");
  const subject = encodeURIComponent(`TripForge Itinerary: ${route}`);
  const body = [
    `✈ TripForge Itinerary`,
    ``,
    `Route: ${route}`,
    `Dates: ${fmtDate(dateStart)} – ${fmtDate(dateEnd)} (${nights} nights)`,
    `Passengers: ${totalPax}`,
    `Airline: ${selAirlineName}`,
    ``,
    `--- Flight Legs ---`,
    ...legs.map(l=>`  ${l.label}: ${fm(l.usd*payingPax)}`),
    ``,
    `Grand Total (est.): ${fm(totalCostUSD)}`,
    ``,
    `Plan your trip at tripforge.app`,
  ].join("\n");
  return `mailto:?subject=${subject}&body=${encodeURIComponent(body)}`;
}

